/* Business Question:
What is the total revenue generated by the store?
*/

SELECT
	ROUND(SUM(order_total), 2) AS total_revenue
FROM orders
WHERE order_status = 'completed';

/* Insight:
The revenue generated by the completed orders in this dataset
was $136,609.35.
This value represents the total realized sales after discounts,
taxes, and shipping adjustments for all completed transactions.
*/

-- =============================================================

/* Business Question:
What is the average order value (AOV)?
*/

SELECT
	ROUND(AVG(order_total), 2) AS avg_order_value
FROM orders
WHERE order_status = 'completed';

/* Insight:
IslamIQ's customers tend to pay an average of $173.80
per order - after discounts, shipping, and taxes.
*/

-- =============================================================

/* Business Question:
Which product generates the most gross profit?
*/

SELECT p.product_id, p.product_name,
	SUM(oi.quantity) AS quantity_sold, 
    ROUND(SUM(oi.quantity * oi.price), 2) AS gross_revenue,
    ROUND(SUM(oi.quantity * p.cost), 2) AS total_cost,
    ROUND(SUM(oi.quantity * (oi.price - p.cost)), 2) AS gross_profit
FROM products p
JOIN order_items oi
	ON p.product_id = oi.product_id
JOIN orders o
	ON oi.order_id = o.order_id
WHERE o.order_status = 'completed'
GROUP BY p.product_id, p.product_name
ORDER BY gross_profit DESC;

/* Insight:
IslamIQ's highest-gross-profit products were the 4 variations of the 
Family Tree of Prophet Muhammad, with the Arabic version in the wooden
frame as the lead product.
NOTE: there were products with higher 'quantity_sold', but it is clear
that does not always equate to what is the most profitable.
*/

