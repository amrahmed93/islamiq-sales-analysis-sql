/* Business Question:
How have revenue and profit changed over time?
*/

SELECT
	DATE_FORMAT(o.order_date, '%Y/%m') AS 'month',
    ROUND(SUM(oi.quantity * oi.price), 2) AS revenue,
    ROUND(SUM(oi.quantity * p.cost), 2) AS total_cost,
    ROUND(SUM(oi.quantity * (oi.price - p.cost)), 2) AS profit
FROM orders o
JOIN order_items oi
	ON o.order_id = oi.order_id
JOIN products p
	ON oi.product_id = p.product_id
WHERE o.order_status = 'completed'
GROUP BY DATE_FORMAT(o.order_date, '%Y/%m')
ORDER BY DATE_FORMAT(o.order_date, '%Y/%m') ASC;

/* Insight:
This shows us IslamIQ's monthly revenue, COGS, and profit per month for 12
months. It's clear there hasn't been much change in profit as it oscillates within
a small range, although at the end, it seems that there was approximately a 20%
dip in profits. 2026/03 is only a third of the month, although seems to be following
similar flow to the month before. Overall - no growth.
*/

