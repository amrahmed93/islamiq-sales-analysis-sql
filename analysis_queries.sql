/* Business Question:
How much revenue is being reduced by discounts, and do discounts affect AOV?
*/

SELECT 
	CASE
		WHEN discount_code IS NULL OR discount_code = '' THEN 'no discount'
        ELSE discount_code
	END AS discount_code,
    COUNT(order_id) AS number_of_uses,
    ROUND(SUM(order_total), 2) AS total_revenue,
    ROUND(AVG(order_total), 2) AS AOV,
    ROUND(SUM(discount_amount), 2) AS total_discount_given
FROM orders
WHERE order_status = 'completed'
GROUP BY CASE
			WHEN discount_code IS NULL OR discount_code = '' THEN 'no discount'
			ELSE discount_code
		END
ORDER BY total_discount_given DESC;

/* Insight:
Most revenue ($88,192) came from full-price orders, indicating strong
organic demand without reliance on discounts. WEB10 (10%) was the most frequently
used discount, with WEB15 (15%) after. AOV of more than 15% less than AOV of WEB10
orders indicates that customers who used WEB15 were more price-sensitive. 
Highest AOV came from 15% discounts given in person, suggesting that real life events
produce higher-intent customers, or increase engagement/trust factor.
*/

