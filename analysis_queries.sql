/* Business Question:
Which regions generate the strongest sales performance?
*/

SELECT c.city, c.country,
	COUNT(DISTINCT o.order_id) AS total_orders,
	ROUND(SUM(o.order_total), 2) AS total_revenue,
	ROUND(AVG(o.order_total), 2) AS aov,
    ROUND(SUM(SUM(o.order_total)) OVER (PARTITION BY c.country), 2) AS country_total_revenue,
    ROUND(SUM(o.order_total) / SUM(SUM(o.order_total)) OVER (PARTITION BY c.country) * 100, 2) AS portion_of_country_total
FROM customers c
JOIN orders o
	ON c.customer_id = o.customer_id
WHERE order_status = 'completed'
GROUP BY c.city, c.country
ORDER BY country_total_revenue DESC, total_revenue DESC;

/* Insight:
This query ranks countries from highest to lowest based on total revenue generated
from each country, telling us the total amount, and further breaking it down by city,
telling us, in order, how much revenue is generated per city. 
*/

