/* Business Question:
Which products generate the most revenue and profit?
*/

SELECT p.product_id, p.product_name,
	SUM(oi.quantity) AS units_sold,
	ROUND(SUM(oi.quantity * oi.price), 2) AS total_revenue,
    ROUND(SUM(oi.quantity * p.cost), 2) AS COGS,
    ROUND(SUM(oi.quantity * (oi.price - p.cost)), 2) AS gross_profit,
    ROUND(SUM(oi.quantity * (oi.price - p.cost)) / NULLIF(SUM(oi.quantity * oi.price), 0) * 100, 2) AS profit_margin
FROM products p
JOIN order_items oi
	ON p.product_id = oi.product_id
JOIN orders o
	ON oi.order_id = o.order_id
WHERE order_status = 'completed'
GROUP BY p.product_id, p.product_name
ORDER BY total_revenue DESC;

/* Insights:
It's clear here that IslamIQ's highest revenue generators, as well as its
highest profit generators are the Family Tree of the Prophet in all its
variations. This shows solid demand, and healthy margins as opposed to other
products in their catalogue. But if we order by profit margin, we find that
unframed 24x36" posters have the highest margins (65%) apart from obvious
digital downloads.
*/