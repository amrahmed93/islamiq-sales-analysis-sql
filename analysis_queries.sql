/* Business Question:
Which sign-up sources bring in the most customers and revenue?
*/

SELECT 
	CASE
		WHEN c.signup_source IS NULL or c.signup_source = '' THEN 'Other'
        ELSE c.signup_source
	END AS signup_source,
COUNT(DISTINCT c.customer_id) AS customers_acquired,
ROUND(SUM(o.order_total), 2) AS total_revenue
FROM customers c
JOIN orders o
	ON c.customer_id = o.customer_id
WHERE o.order_status = 'completed'
GROUP BY
	CASE
		WHEN c.signup_source IS NULL or c.signup_source = '' THEN 'Other'
        ELSE c.signup_source
	END
ORDER BY total_revenue DESC;

/* Insight:
This shows us that more people signed up during checkout (391) as opposed to
those who actually signed up for a discount code (254). This may signal something
about the lack of price-sensitivity of a large portion of customers, or it may
mean that discounts are not promoted as strongly. Majority of total revenue
collected from customers who did not use a discount code.
*/

