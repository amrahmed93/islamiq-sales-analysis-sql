/* Business Question:
Which product categories generate the most gross profit?
*/

SELECT p.category,
	ROUND(SUM(o.quantity * (o.price - p.cost)), 2) AS gross_profit
FROM order_items o
JOIN products p
	ON o.product_id = p.product_id
GROUP BY p.category
ORDER BY gross_profit DESC;

/* Insight:
It's clear that Wall Art is IslamIQ's biggest gross-profit generating category,
at $66k as opposed to apparel, which is next in line at 10% of that amount.
This indicates that Wall Art is the category generating the largest share of
customer spending.
*/

-- =============================================================

/* Business Question:
Which product categories perform best by revenue, profit, and margin?
*/

SELECT p.category,
	ROUND(SUM(o.quantity * o.price), 2) AS gross_revenue,
    ROUND(SUM(o.quantity * p.cost), 2) AS total_cost,
    ROUND(SUM(o.quantity * (o.price - p.cost)), 2) AS gross_profit,
    ROUND(SUM(o.quantity * (o.price - p.cost)) / NULLIF(SUM(o.quantity * o.price), 0), 2) AS margin_percent
FROM order_items o
JOIN products p
	ON o.product_id = p.product_id
GROUP BY p.category
ORDER BY margin_percent DESC;

/* Insight:
Here we can see that the data is slightly skewed due to Digital Downloads
showing as having 'no costs'. This is mostly true, except that Square still
does take a percentage of each transaction as with all other categories. We
still can conclude that Wall Art grosses both the highest revenue and profit
by far, and sits at a decent margin of 44% (pre discounts+taxes). Also, whatever
is in 'other' needs to go. 19% margins mean our efforts are best spent elsewhere.
*/

