/* Business Question:
Which products are purchased most frequently?
*/

SELECT p.product_id, p.product_name,
	SUM(o.quantity) AS quantity_sold
FROM order_items o
JOIN products p
	ON p.product_id = o.product_id
GROUP BY p.product_id, p.product_name
ORDER BY quantity_sold DESC
LIMIT 3;

/* Insight:
This shows us that despite none of these products were in the list of
the highest-grossing-profit products, these are still the most popular
products as they are the most frequently purchased.
*/

